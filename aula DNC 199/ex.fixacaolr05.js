var i = 10; //inicializa a variável com o valor máximo

while (i>=1) {// Continua o loop enquanto i for maior ou igual a 1
    console.log (i); // Exibe o valor atual de i
    i--; // Decrementa i (reduz 1 a cada iteração)

}